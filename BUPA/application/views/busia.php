            <h2>Things to Do and See:</h2>

            <ul>
              <p><i class="bi bi-check-circle"></i><strong>Budalangi Canals</strong>: Embark on a boat safari through the winding Budalangi Canals, a remarkable network of waterways showcasing the wetland ecosystem. Here, you can spot various bird species, including the rare shoebill stork.</p>
              <p><i class="bi bi-check-circle"></i><strong>Nambale Suba Sacred Grove</strong>: Immerse yourself in the spirituality of the Nambale Suba Sacred Grove, a mystical forest believed to be inhabited by ancestral spirits. The tranquil surroundings make it a perfect spot for meditation and reflection.</p>
              <p><i class="bi bi-check-circle"></i><strong>Port Victoria</strong>: Visit Port Victoria, a bustling fishing village that boasts picturesque views of Lake Victoria. Take a boat ride to Rusinga Island and explore the ancient archaeological sites and the charming community.</p>
              <p><i class="bi bi-check-circle"></i><strong>Cultural Festivals</strong>:  Experience the vibrant cultural festivals celebrated throughout the year. The Obusuma Festival and Teso Cultural Festival are two notable events that showcase traditional music, dance, and rituals.</p>
              <p><i class="bi bi-check-circle"></i><strong>Tiriki Cultural Centre</strong>: Delve into the rich heritage of the Tiriki people at the Tiriki Cultural Centre, where you can witness traditional performances and learn about their history and customs.</p>
            
            </ul>

